text = "stay Safe at Home"

#casefold()-returns a string where all the characters are lower case
x = text.casefold()
print(x)

#capitalize()- first character of the string to a capital (uppercase) letter while making all other characters in the string lowercase letters
x = text.capitalize()
print(x)

#find()- finds the first occurrence of the specified value. Returns -1 if the value is not found.
x = text.find('t')
print(x)
x = text.find('T')
print(x)

# isalpha()-returns True if all the characters are alphabet letters. Space is not considered to be alphabet.
x = text.isalpha()
print(x)

#isalnum()-returns True if all the characters are alphanumeric
x = text.isalnum()
print(x)



