# Enumerate a python list and print the counter with the list value
list_1= ['Sridhar','Anu','Manasi','Adithya','Abilash']
print("List:",list_1)
print("Counter with list value:",list(enumerate(list_1)))

# Enumerate a python tuple print the counter with the tuple value
tuple_1=('Aravind','Bharath','Muthu','Sudhakar','Ayyanar')
print("Tuple:",tuple_1)
print("Counter with tuple value",tuple(enumerate(tuple_1)))